class KnowledgeBase:
    """矿山自救知识库 — 关键词检索"""

    def __init__(self, knowledge=None):
        self._kb = knowledge or KNOWLEDGE_BASE

    def search(self, query: str, top_k: int = 2) -> list:
        """Return top_k matching knowledge entries for a query."""
        scored = []
        q_lower = query.lower()
        for title, entry in self._kb.items():
            score = 0
            for kw in entry["keywords"]:
                if kw.lower() in q_lower:
                    score += 1
            if score > 0:
                scored.append((score, title, entry["content"]))
        scored.sort(key=lambda x: x[0], reverse=True)
        return [(title, content) for _, title, content in scored[:top_k]]

    def get_all_topics(self) -> list:
        return list(self._kb.keys())

    def inject_prompt(self, query: str, base_prompt: str) -> str:
        """Build a prompt with relevant knowledge injected."""
        matches = self.search(query, top_k=2)
        if not matches:
            return base_prompt

        knowledge_text = "\n\n".join(f"【{title}】\n{content}" for title, content in matches)
        return (
            f"请参考以下矿山自救知识回答问题。回答要专业、简洁、实用。\n\n"
            f"=== 参考知识 ===\n{knowledge_text}\n=== 知识结束 ===\n\n"
            f"{base_prompt}"
        )
